import java.util.Scanner;

class SolvingForCarrots {
        public static void main(String[] args) {
                Scanner sc = new Scanner(System.in);
                int n = sc.nextInt();
                int p = sc.nextInt();

                sc.close();

                while(n != 0) {
                        Scanner contestants = new Scanner(System.in);
                        n--;
                        contestants.close();
                }

                System.out.println(p);
        }
}
